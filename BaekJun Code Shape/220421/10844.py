n = int(input())

a = [ [0] * 10 for _ in range(n + 1) ]

a[1] = [1] * 10

for i in range(2, n + 1):
    a[i][0] = a[i-1][1]
    a[i][1] = a[i-1][0] + a[i-1][2]
    a[i][2] = a[i-1][1] + a[i-1][3]
    a[i][3] = a[i-1][2] + a[i-1][4]
    a[i][4] = a[i-1][3] + a[i-1][5]
    a[i][5] = a[i-1][4] + a[i-1][6]
    a[i][6] = a[i-1][5] + a[i-1][7]
    a[i][7] = a[i-1][6] + a[i-1][8]
    a[i][8] = a[i-1][7] + a[i-1][9]
    a[i][9] = a[i-1][8]

print( sum(a[n][1:]) % 1000000000)
  
